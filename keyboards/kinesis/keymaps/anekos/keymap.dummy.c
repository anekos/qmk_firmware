bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case WINBOOT:
      if (record->event.pressed) {
        SEND_STRING(SS_TAP(X_DOWN)SS_TAP(X_ENTER));
      }
      return false;
      break;
  }
  return true;
}
